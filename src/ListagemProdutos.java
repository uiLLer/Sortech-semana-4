public class ListagemProdutos {
    public static void main(String[] args) {
        String li = "Livro";
        String ce = "Celular";
        float precoLi = 49.95f;
        float precoCe = 1470.76f;
        System.out.println(">>> Listagem de Produtos");
        System.out.println("_".repeat(50));
        System.out.println("Nome: " + li);
        System.out.println("Data de Nascimento: " + precoLi);
        System.out.println("_".repeat(50));
        System.out.println("Nome: " + ce);
        System.out.println("Data de Nascimento: " + precoCe);
    }
}
